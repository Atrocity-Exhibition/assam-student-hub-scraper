-- 1. Enable Database Extensions
CREATE EXTENSION IF NOT EXISTS pg_trgm;

-- 2. Create Institutions Table
CREATE TABLE IF NOT EXISTS institutions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    slug TEXT NOT NULL UNIQUE,
    description TEXT,
    website TEXT,
    location TEXT,
    logo_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 3. Create Notices Table
CREATE TABLE IF NOT EXISTS notices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    description TEXT,
    source TEXT NOT NULL,
    source_url TEXT NOT NULL,
    category TEXT NOT NULL,
    content_type TEXT,
    institution TEXT NOT NULL,
    institution_slug TEXT,
    posted_at TIMESTAMP WITH TIME ZONE,
    last_seen_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    is_active BOOLEAN DEFAULT true NOT NULL,
    tags TEXT[] DEFAULT '{}',
    slug TEXT NOT NULL UNIQUE,
    raw_html TEXT,
    scraped_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    content_hash TEXT NOT NULL UNIQUE,
    attachment_url TEXT,
    institution_id BIGINT REFERENCES institutions(id) ON DELETE SET NULL,
    is_official BOOLEAN DEFAULT true NOT NULL,
    merged_into_notice_id BIGINT REFERENCES notices(id) ON DELETE SET NULL,
    canonical_url TEXT,
    removed_at TIMESTAMP WITH TIME ZONE,
    reliability_score INTEGER DEFAULT 10,
    scraper_name TEXT,
    search_vector tsvector
);

-- 4. Create Scraper Runs Table
CREATE TABLE IF NOT EXISTS scraper_runs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scraper_name TEXT NOT NULL,
    status TEXT NOT NULL, -- 'running', 'completed', 'failed'
    started_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    completed_at TIMESTAMP WITH TIME ZONE,
    items_scraped INTEGER DEFAULT 0,
    items_inserted INTEGER DEFAULT 0,
    items_updated INTEGER DEFAULT 0,
    errors TEXT[] DEFAULT '{}',
    duration_seconds INTEGER,
    traceback TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 5. Create Saved Notices Table
CREATE TABLE IF NOT EXISTS saved_notices (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    notice_id BIGINT NOT NULL REFERENCES notices(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    UNIQUE(user_id, notice_id)
);

-- 6. Create Search Logs Table
CREATE TABLE IF NOT EXISTS search_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    query TEXT NOT NULL,
    results_count INTEGER DEFAULT 0 NOT NULL,
    search_type TEXT DEFAULT 'fts', -- 'fts' | 'fuzzy' | 'ilike'
    search_duration_ms INTEGER,
    category TEXT,
    user_id UUID REFERENCES auth.users(id) ON DELETE SET NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 7. Create Saved Searches Table
CREATE TABLE IF NOT EXISTS saved_searches (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    query TEXT NOT NULL,
    category TEXT,
    label TEXT,
    notify_enabled BOOLEAN DEFAULT false NOT NULL,
    last_checked_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    UNIQUE(user_id, query, category)
);

